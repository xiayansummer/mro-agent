async def parse_intent(
    user_message: str,
    conversation_context: list[dict] | None = None,
    memory_context: str = "",
    image_base64: str = "",
) -> dict:
    messages = []

    if conversation_context:
        messages.extend(conversation_context)

    # Build user message content — multimodal when image is provided
    if image_base64:
        text = user_message or "请识别图中的工业产品，推荐合适的MRO产品"
        user_content = [
            {
                "type": "image_url",
                "image_url": {"url": f"data:image/jpeg;base64,{image_base64}"},
            },
            {"type": "text", "text": text},
        ]
        messages.append({"role": "user", "content": user_content})
    else:
        messages.append({"role": "user", "content": user_message})

    # Select system prompt and model
    if image_base64:
        system_prompt = VISION_PREFIX + SYSTEM_PROMPT
        model = settings.AI_VISION_MODEL
    else:
        system_prompt = SYSTEM_PROMPT
        model = settings.AI_MODEL

    # Inject memory context when available
    if memory_context:
        system_prompt = (
            system_prompt
            + f"\n\n## 用户历史采购偏好\n\n{memory_context}\n\n"
            "请参考以上历史记录，更准确地理解用户当前需求（如偏好品牌、常用规格、行业背景等）。"
        )

    response = client.chat.completions.create(
        model=model,
        max_tokens=1024,
        messages=[{"role": "system", "content": system_prompt}] + messages,
        extra_body={"enable_thinking": False},
    )

    text = response.choices[0].message.content.strip()

    # Extract JSON from response (handle possible markdown wrapping)
    start = text.find("{")
    end = text.rfind("}") + 1
    if start >= 0 and end > start:
        text = text[start:end]

    return json.loads(text)
